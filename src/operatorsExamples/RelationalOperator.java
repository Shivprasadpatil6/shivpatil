package operatorsExamples;

public class RelationalOperator {
	public static void main(String[]args)
	{
		int a=28;
		int b=30;
		
		System.out.println(a<b);//true
		System.out.println(b>a);//true
		System.out.println(a<=b);// true
		System.out.println(a==b);//false
		System.out.println(b>=a);//true
	}

}
