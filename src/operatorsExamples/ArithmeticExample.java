package operatorsExamples;

public class ArithmeticExample {
	public static void main(String[]args)
	{
		int a=10;
		int b=20;
		int c=40;
		System.out.println(a+b+c);//70
		System.out.println(c-b-a);//10
		System.out.println(a+c);//50
		System.out.println(a/b);//0
		System.out.println(a*c);//400
		System.out.println(a+b/c);//10
		System.out.println(a-c);//-30
		System.out.println(b+c);//60
		
				
		
	}

}
